from enum import Enum, unique


@unique
class BadRequestResponseCode(Enum):
    # 400 Bad Request Error

    # 5 ~ 99 : Uniform
    KAKAO_LOGIN_FAILURE = 401_005

    # 100 ~ 199 : MEMBER
    MEMBER_DUPLICATE_ERROR = 401_100

    # 200 ~ 299 : CERIFICATION

    SMS_CERTIFICATION_REQUEST = 400_200
    SMS_CERTIFICATION_ALREADY_COMPLETE = 400_201

    # 300 ~ 399 : PET
    PET_REGISTERD_LIMIT_EXCEEDED = 400_300
    PET_REGISTERD_DUPLICATE = 400_301
    PET_ATTAACHMENT_REGISTERD_DUPLICATE = 400_303
    PET_ATTAACHMENT_REGISTERD_LIMIT_DUPLICATE = 400_304
    PET_ATTAACHMENT_REGISTERD_ALREADY_PIMART_SET = 400_305

    # 400 ~ 499 : social
    SOCIAL_INVALIDE_RECEIVER_MEMBER = 400_400
    SOCIAL_ALREADY_INVITED_REQUEST = 400_401
    SOCIAL_ALREADY_APPROVAL_REQUEST = 400_402


@unique
class NotFoundResponseCode(Enum):
    # 100 ~ 199 : CERIFICATION
    SMS_CERTIFICATION = 404_100

    # 300 ~ 399 : PET
    DOES_NOT_EXIST_PET = 404_300
